function hover()
{
    var x = document.getElementsByClassName("contact-methods")[0];
    var z = document.getElementsByClassName("contact-methods")[2];
    if (x.style.fontSize = "64px") {
        x.style.fontSize = "10px";
        z.style.fontSize = "10px";
    }
    else {
    x.style.fontSize = "64px";
    z.style.fontSize = "64px";
    }

}